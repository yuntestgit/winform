        如果你双击某个事件的名称，或者在右边的内容出双击，就代表创建该事件。
        在事件右边的内容如果有下拉列表，你就可以选择某个方法作为这个事件的处理方法。

        比如，你误对一个 GroupBox 添加了 Enter 事件。你选择那个 GroupBox，点击属性窗口的闪电图标，找到事件名 Enter ，应该看见如下的情况：

        Enter  | groupBox1_Enter

        把右边的内容删除即可。
